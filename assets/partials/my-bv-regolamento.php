    REGOLAMENTO
</h4>

<p class="text--14">
    La Soc. Bottega sede Amministrativa: Via XXV Aprile. 10 – Cossato (BI), onde promuovere lo smercio degli articoli esposti nei propri punti vendita Diretti situati nel territorio nazionale intende offrire premi certi come ringraziamento per quanto comprato.
</p>
<p class="text--14">
    <strong>PROMOTORE:</strong> Bottega Verde Srl Sede Legale Podere San Luigi – Palazzo Massaini – PIENZA - SI.
</p>
<p class="text--14">
    <strong>AREA:</strong> territorio nazionale – Punti vendita Diretti, Affiliati e Outlet  Bottega Verde
</p>
<p class="text--14">
    <strong>DURATA:</strong> dal 01  Marzo  2015  al  31 Marzo 2016
</p>
<p class="text--14">
    <strong>PARTECIPANTI:</strong> Clienti che effettueranno acquisti presso i punti vendita Diretti, Affiliati e Outlet
    di Bottega Verde presenti sul territorio nazionale,
    in possesso della Carta Fedeltà Bottega Verde. La partecipazione viene estesa anche ai clienti che effettueranno
    acquisti tramite il sito internet www.bottegaverde.it, e tramite la vendita per corrispondenza.
</p>
<p class="text--14">
    <strong>MODALITA’:</strong> il piano dell’operazione prevede di dare la possibilità ai clienti che effettuano acquisti
    presso i punti vendita Bottega Verde, di ricevere degli omaggi effettuando una raccolta punti, denominata “Collezione
    Regali Grandi Marche 2015-2016”. La presente raccolta punti verrà estesa anche ai clienti che effettueranno acquisti
    tramite il sito internet  www.bottegaverde.it , tramite telefono e tramite i buoni d’ordine inseriti nei cataloghi distribuiti con
    la vendita per corrispondenza.
</p>
<p class="text--14">
    Verrà assegnato Un punto ( in seguito denominati: “punti natura”)  ogni € 1 (Euro) di spesa effettuata dal cliente.
    Per effettuare la raccolta punti, al cliente verrà consegnata una “tessera”  in seguito denominata  “Carta Fedeltà”.
    <br>
    La Carta Fedeltà Bottega Verde e gratuita e può essere richiesta in qualsiasi momento presso  i negozi Diretti, Affiliati
    e Outlet presenti sul territorio nazionale, oppure tramite posta o tramite il sito internet www.bottegaverde.it,
    secondo le modalità di seguito specificate.
</p>
<p class="text--14">
    <strong>Tramite i Negozi Bottega Verde.</strong>
</p>
<p class="text--14">
    Il nuovo cliente potrà richiedere la propria Carta Fedeltà presso i negozi Diretti, Affiliati e Outlet Bottega Verde.
    La commessa del negozio consegnerà al cliente un modulo detto scheda di benvenuto,  che il cliente  dovrà compilare con
    i propri dati anagrafici e firmare per adesione. I dati anagrafici del cliente saranno codificati con un “codice cliente”
    e saranno collegati a livello informatico con il codice alfanumerico  presente  anche  come codice a barre sulla Carta Fedeltà appena consegnata.
</p>
<p class="text--14">
    La carta Fedeltà verrà consegnata immediatamente al cliente e sarà valida già al momento del rilascio.
    La Carta Fedeltà non ha scadenza ed è sempre valida.
</p>
<p class="text--14">
    <strong>Tramite Posta/Vendita per corrispondenza.</strong>
</p>
<p class="text--14">
    I clienti già in possesso della Carta Fedeltà ritirata presso i negozi Bottega Verde, potranno partecipare alla raccolta
    Punti Natura effettuando acquisti anche tramite gli ordini inviati per posta, Fax o effettuati telefonicamente a Bottega Verde.
    Il codice alfanumerico stampato sulla Carta Fedeltà dovrà essere riportato correttamente dal cliente nell'apposito spazio inserito
    nel Buono d'0rdine utilizzato per gli acquisti dei prodotti, oppure comunicato telefonicamente all'operatrice al momento dell'ordine.
    Questo per dare modo a Bottega Verde di identificare il cliente già in possesso della Carta Fedeltà
    ed effettuare l'accredito dei punti accumulati sulla tessera di riferimento.
</p>
<p class="text--14">
    Si specifica che i punti natura verranno accreditati sulla carta fedeltà data dal cliente come riferimento e non sul nominativo intestatario dell'ordine.
</p>
<p class="text--14">
    I clienti non ancora in possesso della Carta Fedeltà, potranno farne richiesta indicando l'adesione nell'apposito spazio
    inserito nel buono d'ordine utilizzato per gli acquisti.
    <br>
    Bottega Verde provvederà ad attribuire al cliente il codice alfanumerico identificativo che dovrà essere utilizzato per il conteggio dei Punti Natura.
    <br>
    Tale codice verrà inviato anche tramite posta direttamente al domicilio del cliente.
</p>
<p class="text--14">
    <strong>Tramite il Sito Internet.</strong>
</p>
<p class="text--14">
    I clienti, già in possesso della Carta Fedeltà ritirata presso i negozi Bottega Verde, potranno ottenere l'accredito dei punti natura,
    inserendo il codice alfanumerico, presente sulla carta fedeltà di riferimento, nell'apposito spazio predisposto in fase di
    completamento dell'ordine di acquisto.
</p>
<p class="text--14">
    I clienti non ancora in possesso della Carta Fedeltà, potranno farne richiesta confermando l'adesione nell'apposito spazio
    predisposto, che comparirà automaticamente durante la fase di completamento dell'ordine di acquisto.
    <br>
    Bottega Verde attribuirà il codice alfanumerico identificativo della Carta Fedeltà all'ordine appena inserito.
    Successivamente invierà per posta direttamente al cliente una tessera che riporterà tale numero identificativo.
</p>